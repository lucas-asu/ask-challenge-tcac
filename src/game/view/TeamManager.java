    public TeamManager(PlayerController pc, TeamController tc, MainWindow mw) {
        initComponents();
        this.controller = pc;
        this.teamController = tc;
        this.window = mw;
        List<Player> pls = new ArrayList(teamController.team.getMembers().values());
        DefaultListModel model = new DefaultListModel();
        members.setModel(model);
        for (Player p : pls) {
            if (!controller.player.getID().equalsIgnoreCase(p.getID())) {
                model.addElement(p);
            }
        }

    }

