    private void jbLeaderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbLeaderActionPerformed
        Player p = (Player) members.getSelectedValue();
        try {
            controller.factory.getAuthenticationInterface().changeLeader(teamController.team.getID(), p);
            window.leaveLeadership();
            JOptionPane.showMessageDialog(this, "The player " + p.getName() + " is the new leader!");
            this.dispose();
        } catch (RemoteException ex) {
            Logger.getLogger(TeamManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbLeaderActionPerformed

