    public InvitationChallenge(PlayerController pc, TeamController tc, Invitation inv, MainWindow mw) {
        initComponents();
        this.controller = pc;
        this.teamController = tc;
        this.invitation = inv;
        this.window = mw;

        try {
            if (teamController.team.getID().equalsIgnoreCase(inv.getFrom())) {
                tm = controller.factory.getAuthenticationInterface().getTeam(inv.getTo());
                msg.setText("Your team will challenge "+inv.getPoints()+" points with the team '" + tm.getName() + "'. Do you will accept?");
            } else {
                tm = controller.factory.getAuthenticationInterface().getTeam(inv.getFrom());
                msg.setText("The team '" + tm.getName() + "' is challenging "+inv.getPoints()+" points with your team. Do you will accept?");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(InvitationChallenge.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

