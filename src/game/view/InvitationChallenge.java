    private void accepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accepActionPerformed
        try {
            invitation.setAnswer(Util.INVITATION_ACCEPTED);
            if (teamController.team.getID().equalsIgnoreCase(invitation.getFrom())) {
                controller.factory.getInvitationChallengeInterface().answerInternalInvitation(invitation);
            } else {
                controller.factory.getInvitationChallengeInterface().answerExternalInvitation(invitation);
            }
            controller.sendMessageTeam(controller.player.getName() + " accepted the challenge", teamController.team);

        } catch (RemoteException ex) {
            Logger.getLogger(InvitationChallenge.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();

    }//GEN-LAST:event_accepActionPerformed

