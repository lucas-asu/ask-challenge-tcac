    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Team teamA = teamController.team;
        Team teamB = (Team) jlTeams.getSelectedValue();
        int numRounds = Integer.parseInt((String) nbRd.getSelectedItem());
        int points = Integer.parseInt(pt.getText());

        Challenge cha = new Challenge(teamA, teamB, numRounds, points);

        Invitation inv = new Invitation(cha.getID(), controller.player.getID(), teamA.getID(), teamB.getID(),points);

        try {
            controller.factory.getChallengeInterface().newChallenge(cha);
            teamController.sendInvitationChallenge(cha);
            JOptionPane.showMessageDialog(this, "Challenge was created! Wait for the answers");
            this.dispose();
        } catch (RemoteException ex) {
            Logger.getLogger(CreateChallenge.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


