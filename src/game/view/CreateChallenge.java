    public CreateChallenge(PlayerController pc, TeamController tc, MainWindow win) {
        initComponents();
        this.controller = pc;
        this.teamController = tc;
        this.window = win;

        List<Team> teams = teamController.getAllTeams();
        if (teams.size() > 1) {
            DefaultListModel model = new DefaultListModel();
            jlTeams.setModel(model);
            for (Team t : teams) {
                if (!teamController.team.getID().equalsIgnoreCase(t.getID())) {
                    model.addElement(t);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Don't have any team to challenge!");
            this.setVisible(false);
            this.dispose();
        }
    }

